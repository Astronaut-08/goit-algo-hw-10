import scipy.integrate as spi

# –û–±—á–∏—Å–ª–µ–Ω–Ω—è —ñ–Ω—Ç–µ–≥—Ä–∞–ª–∞
result, error = spi.quad(f, a, b)

print("–Ü–Ω—Ç–µ–≥—Ä–∞–ª: ", result, error)


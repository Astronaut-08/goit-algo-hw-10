import random

def monte_carlo(a, b, experiment):
    '''–ø—Ä–æ–≤–æ–¥–∏—Ç—å –µ–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç–∏ –≤ –∑–∞–¥–∞–Ω—ñ–π –∫—ñ–ª—å–∫–æ—Å—Ç—ñ —Å–ø—Ä–æ–±'''
    y_max = f(b)
    average_area = 0

    for _ in range(experiment):
        ins_point = 0

        for _ in range(10_000):
            if random.uniform(0, y_max) <= f(random.uniform(a, b)):
                ins_point += 1

        average_area += (ins_point/10_000) * ((b-a) * y_max)

    average_area /= experiment
    return average_area

print('Monte Carlo: ', monte_carlo(a, b, 1000))

import matplotlib.pyplot as plt
import numpy as np

# –í–∏–∑–Ω–∞—á–µ–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó —Ç–∞ –º–µ–∂—ñ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è
def f(x):
    return x ** 2

a = 0  # –ù–∏–∂–Ω—è –º–µ–∂–∞
b = 2  # –í–µ—Ä—Ö–Ω—è –º–µ–∂–∞

# –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –¥—ñ–∞–ø–∞–∑–æ–Ω—É –∑–Ω–∞—á–µ–Ω—å –¥–ª—è x
x = np.linspace(-0.5, 2.5, 400)
y = f(x)

# –°—Ç–≤–æ—Ä–µ–Ω–Ω—è –≥—Ä–∞—Ñ—ñ–∫–∞
fig, ax = plt.subplots()

# –ú–∞–ª—é–≤–∞–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó
ax.plot(x, y, 'r', linewidth=2)

# –ó–∞–ø–æ–≤–Ω–µ–Ω–Ω—è –æ–±–ª–∞—Å—Ç—ñ –ø—ñ–¥ –∫—Ä–∏–≤–æ—é
ix = np.linspace(a, b)
iy = f(ix)
ax.fill_between(ix, iy, color='gray', alpha=0.3)

# –ù–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è –≥—Ä–∞—Ñ—ñ–∫–∞
ax.set_xlim([x[0], x[-1]])
ax.set_ylim([0, max(y) + 0.1])
ax.set_xlabel('x')
ax.set_ylabel('f(x)')

# –î–æ–¥–∞–≤–∞–Ω–Ω—è –º–µ–∂ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è —Ç–∞ –Ω–∞–∑–≤–∏ –≥—Ä–∞—Ñ—ñ–∫–∞
ax.axvline(x=a, color='gray', linestyle='--')
ax.axvline(x=b, color='gray', linestyle='--')
ax.set_title('–ì—Ä–∞—Ñ—ñ–∫ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è f(x) = x^2 –≤—ñ–¥ ' + str(a) + ' –¥–æ ' + str(b))
plt.grid()
plt.show()
